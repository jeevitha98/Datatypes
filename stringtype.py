s="food is love "
print(s)

#indexing
print(s[3])

#display string several times
print(s*3)

#length of string
print(len(s))

#slicing
print(s[8:12]) #from index 8 to 12
print(s[:8]) #from start till 8th char
print(s[3:-1]) #from 8th char till end of str

print(s[3::-1]) #3rd arg for reversing string
print(s[::-1]) #complete string is reversed